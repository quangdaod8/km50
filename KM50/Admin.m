-(void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath {
    if(indexPath.row >= 0 && indexPath.row < 4) [self performSegueWithIdentifier:@"addetail" sender:indexPath];
}

-(void)prepareForSegue:(UIStoryboardSegue *)segue sender:(id)sender {
    if([segue.identifier isEqualToString:@"addetail"]){
        NSIndexPath *index = (NSIndexPath*)sender;
        NetworkDetail *detail = segue.destinationViewController;
        detail.network = _data[index.row];
    }
}

